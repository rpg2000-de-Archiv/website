                            <td colspan="3" class="navigation_innen">
                                <p><b>17.04.2010, <a name="n3">Die Zukunft</a> - <a href="mailto:webmaster@rpg2000.de" class="home">Brainstormer</a></b></p>
                                Hallo, liebe Besucher. <br />
                                <br />
                                Sicherlich ist euch aufgefallen, dass es bereits seit über einem Jahr kein Update mehr hier auf der Hauptseite des Quartiers gegeben hat. Als Gründe könnte ich verschiedene Dinge anführen, darunter Zeitmangel zur einen, Motivationslosigkeit zur anderen Zeit. Zu veraltet ist die Technik der Seite und zu groß die daraus stammende Zentralisierung, dass alles über eine Person, nämlich mich, laufen muss. <br />
                                <br />
                                Fakt ist: Ich sehe keine Zukunft für die Seite, wie sie jetzt im Moment ist. Es wird kein Ressourcen- oder Spieleupdate mehr für die Seite erscheinen.<br />
                                <br />
                                Wir können also Ade sagen zu dieser Seite, die euch und mir einige Zeit - vielleicht sogar lange Zeit - viel Spaß gemacht hat. <br />
                                Wie geht es weiter? Während des letzten Jahres habe ich mir immer wieder Gedanken über die Zukunft gemacht, Dinge ausprobiert, verschiedene technische Möglichkeiten ausgetestet. Dieses Austesten hat bereits handfeste Züge angenommen und schreitet in unregelmäßigen Abständen immer weiter fort. In diesem Sinne möchte ich hier noch keine neue Version einer an das Web 2.0 angepassten Seite ankündigen, denn es gibt noch sehr viel zu tun und es wird an Baustellen aller möglichen Arten gleichzeitig gewerkelt. Die Seite wird mit Sicherheit nicht in der nächsten Zeit, das heißt innerhalb einiger Wochen, fertiggestellt werden, noch kann ich eine Fertigstellung überhaupt garantieren. Dennoch bin ich zuversichtlich, dass das hier nicht das Ende bedeutet.<br />
                                <br />
                                Schaut mal ab und zu rein, wenn es Fortschritte gibt, lasse ich es euch hier wissen.
